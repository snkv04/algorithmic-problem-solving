ll gcd(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    ll A = max(a, b), B = min(a, b);
    while (B != 0) {
        ll R = A % B;
        A = B;
        B = R;
    }
    return A;
}

ll lcm(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    return a / gcd(a, b) * b;
}

void dfs(int node, int parent, vector<vector<int>> &adj, vector<vector<ll>> &mem) {
    for (int next : adj[node]) {
        if (next == parent) continue;

        dfs(next, node, adj, mem);

        mem[node][0] = (mem[node][0] * mem[next][1]) % MOD;
        mem[node][1] = mem[node][1] * (mem[next][0] + mem[next][1]) % MOD;
    }
}

void solve() {
    int n;
    cin >> n;
    vector<vector<int>> adj(n);
    for (int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    vector<vector<ll>> mem(n, vector<ll>(2, 1));
    dfs(0, -1, adj, mem);
    cout << ((mem[0][0] + mem[0][1]) % MOD) << endl;
}

