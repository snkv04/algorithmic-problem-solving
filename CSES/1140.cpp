ll gcd(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    ll A = max(a, b), B = min(a, b);
    while (B != 0) {
        ll R = A % B;
        A = B;
        B = R;
    }
    return A;
}

ll lcm(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    return a / gcd(a, b) * b;
}

void solve() {
    int n;
    cin >> n;
    vector<array<int, 3>> segments(n);
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < 3; ++j) {
            cin >> segments[i][j];
        }
    }
    sort(segments.begin(), segments.end());
    reverse(segments.begin(), segments.end());

    map<int, ll> mem;
    mem[2e9] = 0;
    for (auto [a, b, p] : segments) {
        ll with = mem.upper_bound(b)->second + p;
        ll without = mem.lower_bound(a)->second;  // if mem[a] already exists, then it'll be counted in `without`
        mem[a] = max(with, without);
    }
    cout << mem.begin()->second << endl;
}

