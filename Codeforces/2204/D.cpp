ll gcd(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    ll A = max(a, b), B = min(a, b);
    while (B != 0) {
        ll R = A % B;
        A = B;
        B = R;
    }
    return A;
}

ll lcm(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    return a / gcd(a, b) * b;
}

void solve() {
    int n, m;
    cin >> n >> m;
    adj = vector<vector<int>>(n);
    while (m--) {
        int u, v;
        cin >> u >> v;
        --u; --v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    int ans = 0;
    color = vector<int>(n, 0);
    visited = vector<bool>(n, false);
    for (int i = 0; i < n; ++i) {
        if (!color[i]) {
            bool bipartite = true;
            int nodes = 0;
            queue<int> q;
            q.push(i);
            color[i] = 1;
            int ones = 1;
            while (q.size()) {
                int node = q.front();
                q.pop();
                if (visited[node]) continue;

                visited[node] = true;
                nodes += 1;

                for (int next : adj[node]) {
                    if (!color[next]) {
                        color[next] = 3 - color[node];
                        q.push(next);
                        if (color[next] == 1) ones += 1;
                    } else if (color[next] == color[node]) {
                        bipartite = false;  // don't want to break; need to color all nodes in the component
                    }
                }
            }

            if (bipartite) {
                ans += max(ones, nodes - ones);
            }
        }
    }
    cout << ans << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
