ll gcd(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    ll A = max(a, b), B = min(a, b);
    while (B != 0) {
        ll R = A % B;
        A = B;
        B = R;
    }
    return A;
}

ll lcm(ll a, ll b) {
    if (a < 0) a = -a;
    if (b < 0) b = -b;
    return a / gcd(a, b) * b;
}

void solve() {
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;
    vector<int> a(n);
    cin >> a;

    int ans = -1, l = 0, r = 1e9;
    while (l <= r) {
        int m = (l + r) / 2;

        int segments = 0;
        bool last = false;
        for (int i = 0; i < n; ++i) {
            if (a[i] <= m) continue;

            if (s[i] == 'B') {
                if (!last) ++segments;
                last = true;
            } else {
                last = false;
            }
        }

        if (segments <= k) {
            ans = m;
            r = m - 1;
        } else {
            l = m + 1;
        }
    }
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
